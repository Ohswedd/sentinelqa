def test_test_command_applies_url_browser_and_retries_overrides(
    runner: CliRunner, fresh_project: Path, monkeypatch: pytest.MonkeyPatch
) -> None:
    monkeypatch.chdir(fresh_project)
    write_config(fresh_project)
    _seed_specs(fresh_project, ["a.spec.ts"])
    outcome = _build_outcome("functional", status="passed")
    captured = _patch_runner(monkeypatch, outcome)

    cli = build_app()
    result = runner.invoke(
        cli,
        [
            "test",
            "--url",
            "http://localhost:9000",
            "--browser",
            "firefox",
            "--retries",
            "3",
            "--workers",
            "2",
        ],
    )

    assert result.exit_code == 0, result.stdout
    # The runner was constructed with our overridden config.
    cfg = captured["init"]["config"]
    assert str(cfg.target.base_url).startswith("http://localhost:9000")
    assert cfg.runner.browser == "firefox"
    assert cfg.runner.retries.max == 3
    assert captured["invocation"].workers == 2


