def _seed_specs(project: Path, names: list[str]) -> list[Path]:
    """Create empty Playwright spec files so ``sentinel test`` finds them."""

    spec_root = project / "tests" / "sentinel"
    spec_root.mkdir(parents=True, exist_ok=True)
    out: list[Path] = []
    for name in names:
        path = spec_root / name
        path.write_text("// generated by SentinelQA tests\n", encoding="utf-8")
        out.append(path)
    return out


def _build_outcome(module_name: str, *, status: str, flake_rate: float = 0.0) -> RunnerOutcome:
    tests = (
        TestExecution(
            test_id="t1",
            title="sample",
            file="sample.spec.ts",
            status="passed" if status == "passed" else "failed",
            duration_ms=400,
            retries=0,
        ),
    )
    final_status: str = "passed" if status == "passed" else "failed"
    return RunnerOutcome.build(
        module_name=module_name,
        module_id="MOD-CMDAAAAAAAAA",
        status=final_status,  # type: ignore[arg-type]
        tests=tests,
        duration_ms=400,
        environment=None,
    )


def _patch_runner(monkeypatch: pytest.MonkeyPatch, outcome: RunnerOutcome) -> dict[str, Any]:
    captured: dict[str, Any] = {}

    class _StubLocalRunner:
        def __init__(self, *args: Any, **kwargs: Any) -> None:
            captured["init"] = kwargs

        def run(self, invocation: Any) -> RunnerOutcome:
            captured["invocation"] = invocation
            return outcome

    monkeypatch.setattr(
        "sentinel_cli.commands.test_cmd.LocalRunner",
        _StubLocalRunner,
    )
    return captured


def test_test_command_runs_local_runner_and_exits_zero(
    runner: CliRunner, fresh_project: Path, monkeypatch: pytest.MonkeyPatch
) -> None:
    monkeypatch.chdir(fresh_project)
    write_config(fresh_project)
    _seed_specs(fresh_project, ["login.spec.ts"])
    outcome = _build_outcome("functional", status="passed")
    captured = _patch_runner(monkeypatch, outcome)

    cli = build_app()
    result = runner.invoke(cli, ["test", "--module", "functional"])

    assert result.exit_code == 0, result.stdout + result.stderr
    assert "run_id" in result.stdout
    assert "status     : passed" in result.stdout
    assert captured["invocation"].module_name == "functional"


