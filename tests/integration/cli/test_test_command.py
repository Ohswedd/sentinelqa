def test_test_command_flake_gate_triggers_exit_one(
    runner: CliRunner, fresh_project: Path, monkeypatch: pytest.MonkeyPatch
) -> None:
    monkeypatch.chdir(fresh_project)
    write_config(fresh_project)
    _seed_specs(fresh_project, ["a.spec.ts"])

    # Build an outcome whose flake_rate exceeds policy.max_flake_rate.
    flaky_outcome = RunnerOutcome.build(
        module_name="functional",
        module_id="MOD-FLAKAAAAAAAA",
        status="passed",
        tests=(
            TestExecution(
                test_id="t1",
                title="flaky test",
                file="a.spec.ts",
                status="flaky",
                duration_ms=900,
                retries=1,
            ),
        ),
        duration_ms=900,
        environment=None,
    )
    _patch_runner(monkeypatch, flaky_outcome)

    cli = build_app()
    result = runner.invoke(cli, ["test"])
    # Flake rate above policy.max_flake_rate (3%) → exit 1.
    assert result.exit_code == 1


